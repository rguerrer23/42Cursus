#include "inc/minishell.h"

/* Esta funcion cambia el directorio actual de trabajo. */

int	ft_cd(char *path)
{
	if (chdir(path) != 0)
	{
		write(STDERR_FILENO, "cd failed", 9);
		return (EXIT_FAILURE);
	}
	return (EXIT_SUCCESS);
}
