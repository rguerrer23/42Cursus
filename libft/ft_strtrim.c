#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	int		x;
	char	*ptr;

	if (!s1 || !set)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	x = ft_strlen(s1);
	while (x && ft_strchr(set, s1[x]))
		x--;
	ptr = ft_substr(s1, 0, x + 1);
	return (ptr);
}
